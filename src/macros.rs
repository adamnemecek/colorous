macro_rules! color {
    ($hex:literal) => {{
        use crate::macros::HEX;
        let [rh, rl, gh, gl, bh, bl] = *$hex;
        crate::Color {
            r: (HEX[rh as usize] + 1 - 1) * 16 + (HEX[rl as usize] + 1 - 1),
            g: (HEX[gh as usize] + 1 - 1) * 16 + (HEX[gl as usize] + 1 - 1),
            b: (HEX[bh as usize] + 1 - 1) * 16 + (HEX[bl as usize] + 1 - 1),
        }
    }};
}

macro_rules! colors {
    ($($hex:literal),* $(,)?) => {
        &[$(color!($hex)),*]
    };
}
